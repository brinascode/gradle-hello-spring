var add = (num1,num2)=>{
    return (num1+num2)
}

var subtract = (num1,num2)=>{
    return (num1-num2)
}

var multiply = (num1,num2)=>{
    return (num1 * num2)
}

var divide = (num1,num2)=>{
    return (num1/num2)
}

module.exports = {
    add:add,
    subtract:subtract,
    multiply:multiply,
    divide:divide
}